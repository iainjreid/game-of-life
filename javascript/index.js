/**
 * Clear the console and print the grid to the host TTY.
 */
function printGrid(grid) {
  console.clear();
  console.log(grid.map(_ => _.map(_ => !!_ ? "██" : "  ‎").join("")).join("\n"));
}

/**
 * Retrieve a copy of the grid in its current state.
 */
function copyGrid(grid) {
  return JSON.parse(JSON.stringify(grid));
}

/**
 * Check to see if the cell at the provided coords is active.
 */
function isCellActive(grid, x, y) {
  return grid[y] && grid[y][x]
}
