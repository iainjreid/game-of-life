// A list of the neighouring coordinates (relative) for a cell
coords =
  [-1, 0, 1].collect { y ->
    [-1, 0, 1].collect { x ->
      (y != 0 || x != 0) ? [y, x] : []
    }.findAll { it.size }
  }.inject([], { a, b -> a + b })

def step(grid) {
  printGrid(grid)

  nextGrid =
    grid.withIndex().collect { line, y ->
      line.withIndex().collect { cell, x ->
        liveNeighbours = countLiveNeighbours(grid, y, x)
        cell
          ? liveNeighbours == 2 || liveNeighbours == 3
          : liveNeighbours == 3
      }
    }

  sleep(100)
  step(nextGrid)
}

def printGrid(grid) {
  out =
    grid.collect { line ->
      line.collect { cell ->
        cell ? "██" : "  "
      }.join("")
    }.inject("", { a, b -> "$a\n$b" })

  print "\033[H\033[2J$out"
}

def countLiveNeighbours(grid, y, x) {
  coords.collect { coord ->
    dy = coord[0] + y
    dx = coord[1] + x

    0 <=  dy && dy < grid.size() &&
      0 <=  dx && dx < grid[dy].size() ? grid[dy][dx] : false
  }.findAll { it }.size
}

