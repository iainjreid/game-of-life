import 'dart:io';
import 'dart:math';

class Grid {
  List<List<int>> grid;

  Grid(this.grid);

  void show() =>
    print('\x1B[H\x1B[2J' + grid.formatted);

  void step() =>
    grid.setAll(0, _stepGrid());

  List<List<int>> _stepGrid() =>
    sequence(grid.length).map((y) => _stepLine(y)).toList();

  List<int> _stepLine(int y) =>
    sequence(grid[y].length).map((x) => _stepCell(y, x)).toList();

  int _stepCell(int y, int x) {
    var liveNeighbours = _countLiveNeighbours(y, x);

    return (grid[y][x] == 1)
      ? (liveNeighbours == 3 || liveNeighbours == 2) ? 1 : 0
      : (liveNeighbours == 3) ? 1 : 0;
  }

  int _countLiveNeighbours(int y, int x) {
    var count = 0;

    var ymin = max(0, y - 1);
    var xmin = max(0, x - 1);

    var ymax = min(grid.length, y + 2);
    var xmax = min(grid[y].length, x + 2);

    range(ymin, ymax).forEach((dy) =>
      range(xmin, xmax).forEach((dx) =>
        count += (dy != y || dx != x) ? grid[dy][dx] : 0));

    return count;
  }
}

/// Generate a List of ascending numbers.
List<int> sequence(int n) =>
  List<int>.generate(n, (i) => i);

/// Generate a List of numbers between two bounds.
List<int> range(int start, int stop) =>
  List<int>.generate(stop - start, (i) => i + start);

extension on List<List<int>> {
  String get formatted =>
		fold('', (out, value) => out + '\n' + value.formatted);
}

extension on List<int> {
  String get formatted =>
    fold('', (out, value) => out + (value == 1 ? '██' : '  '));
}

