package main

import (
  "fmt"
  "time"
)

type Grid [24][50]int

func step(grid Grid) {
  printGrid(grid)
  var nextGrid Grid

  for y := 0; y < len(grid); y++ {
    for x := 0; x < len(grid[y]); x++ {
      var liveNeighbours int = countLiveNeightbours(grid, x, y)

      if grid[y][x] == 1 {
        if liveNeighbours == 2 || liveNeighbours == 3 {
          nextGrid[y][x] = 1
        } else {
          nextGrid[y][x] = 0
        }
      } else {
        if liveNeighbours == 3 {
          nextGrid[y][x] = 1
        } else {
          nextGrid[y][x] = 0
        }
      }
    }
  }

  time.Sleep(100 * time.Millisecond)
  step(nextGrid)
}

func printGrid(grid Grid) {
  var out = ""

  for y := 0; y < len(grid); y++ {
    for x := 0; x < len(grid[y]); x++ {
      switch cell := grid[y][x]; cell {
      case 1:
        out += "██"
      case 0:
        out += "  "
      }
    }

    out += "\n"
  }

  fmt.Printf("\033[H\033[2J%s", out)
}

func countLiveNeightbours(grid Grid, x int, y int) int {
  var count = 0

  for dy := y - 1; dy <= y + 1; dy++ {
    for dx := x - 1; dx <= x + 1; dx++ {
      if !(dy == y && dx == x) {
        if 0 <= dy && dy < len(grid) && 0 <= dx && dx < len(grid[dy]) {
          count += grid[dy][dx]
        }
      }
    }
  }

  return count
}

