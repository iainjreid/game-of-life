def step (grid)
  print_grid(grid)
  grid_copy = clone_grid(grid)

  for y in 0...grid.length
    for x in 0...grid[y].length
      live_neighbours = count_live_neighbours(y, x, grid)

      if grid[y][x] == 1
        if live_neighbours == 2 or live_neighbours == 3
          grid_copy[y][x] = 1
        else
          grid_copy[y][x] = 0
        end
      else
        if live_neighbours == 3
          grid_copy[y][x] = 1
        else
          grid_copy[y][x] = 0
        end
      end
    end
  end

  sleep(0.1)
  step(grid_copy)
end

def print_grid (grid)
  system "clear"

  for line in grid
    puts line.reduce("") { |l, c| l + (c > 0 ? "██" : "  ‎") }
  end
end

def clone_grid (grid)
  grid.clone.map { |line| line.clone }
end

def count_live_neighbours (y, x, grid)
  count = 0

  for dy in -1..1
    for dx in -1..1
      if not (dy == 0 and dx == 0)
        if grid[y + dy] and (grid[y + dy][x + dx] === 1)
          count += 1
        end
      end
    end
  end

  count
end

