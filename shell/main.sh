      N)
        x=0; y=$((y+1)) ;;
      *)
        printf "\nUnrecognised cell: '%s'\n" $cell; exit 1
    esac

    next="$next $cell"
  done

  set -- $next

  #
  # Take a nap, we deserve it!
  #
  sleep .1
