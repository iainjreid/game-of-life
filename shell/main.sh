          if [ $dy != 0  ] || [ $dx != 0  ]; then
            sy=$((y+dy))
            sx=$((x+dx))

            if [ $sy -ge 0 ] && [ $sy -lt $h ] && [ $sx -ge 0 ] && [ $sx -lt $w ]; then
