function show(grid)
  local out = ""

  for y = 1, #grid do
    for x = 1, #grid[y] do
      if grid[y][x] == 1 then
        out = out .. "██"
      else
        out = out .. "  "
      end
    end
    out = out .. "\n"
  end

  os.execute("clear")
  print(out)
end

function step(grid)
  local next = {}

  for y, line in ipairs(grid) do
    next[y] = {}

    for x, cell in ipairs(line) do
      local live_neighbours = count_live_neighbours(grid, y, x)

      if cell == 1 then
        if live_neighbours == 3 or live_neighbours == 2 then
          next[y][x] = 1
        else
          next[y][x] = 0
        end
      else
        if live_neighbours == 3 then
          next[y][x] = 1
        else
          next[y][x] = 0
        end
      end
    end
  end

  return next
end

function count_live_neighbours(grid, y, x)
  local count = 0

  local ymin = math.max(1, y-1)
  local xmin = math.max(1, x-1)

  local ymax = math.min(table.getn(grid), y+1)
  local xmax = math.min(table.getn(grid[y]), x+1)

  for dy = ymin, ymax do
    for dx = xmin, xmax do
      if dy ~= y or dx ~= x then
        count = count + grid[dy][dx]
      end
    end
  end

  return count
end

