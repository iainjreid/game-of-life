use std::{thread, time};

type Grid = Vec<Vec<i32>>;

fn print_grid(grid: &Grid) {
  let out =
    grid.iter().map(|line| {
      line.iter().map(|cell| {
        return if cell == &1i32 { "██" } else { "  " };
      }).collect::<Vec<_>>().concat()
    }).collect::<Vec<_>>().join("\n");

  print!("\x1B[H\x1B[2J{}", out);
}

fn count_live_neighbouts(grid: &Grid, y: usize, x: usize) -> i32 {
  let mut count: i32 = 0;

  // Store the minimum values for the axis
  let ymin: usize = if y == 0 { 0 } else { y - 1 };
  let xmin: usize = if x == 0 { 0 } else { x - 1 };

  // Store the maximum values for the axis
  let ymax: usize = if y == (grid.len() - 1) { y } else { y + 1 };
  let xmax: usize = if x == (grid[y].len() - 1) { x } else { x + 1 };

  for dy in ymin..=ymax {
    for dx in xmin..=xmax {
      if dy != y || dx != x {
        count += grid[dy][dx];
      }
    }
  }

  return count;
}

